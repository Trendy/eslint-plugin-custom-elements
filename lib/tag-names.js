const PCENChar = [
  '[',
  '-',
  '\\.',
  '0-9',
  '_',
  'a-z',
  '\\u00B7', // Â·
  '\\u00C0-\\u00D6',
  '\\u00D8-\\u00F6',
  '\\u00F8-\\u037D',
  '\\u037F-\\u1FFF',
  '\\u200C-\\u200D',
  '\\u203F-\\u2040',
  '\\u2070-\\u218F',
  '\\u2C00-\\u2FEF',
  '\\u3001-\\uD7FF',
  '\\uF900-\\uFDCF',
  '\\uFDF0-\\uFFFD',
  '\\u10000-\\uEFFFF',
  ']'
].join('')
const customElementNameRegExp = new RegExp(`^[a-z]${PCENChar}*-${PCENChar}*$`)

module.exports.validTagName = name => customElementNameRegExp.test(name)
